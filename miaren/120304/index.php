<span style="font-size:2em; font-weight:bold">Horse Trial at Willow Draw<br />
</span><br />
    It was a great day. The weather was perfect and schooling the day before had made Miaren a bit tired.<br />
    Not the best way to go into dressage, but comforting when I thought of cross country
    coming up.<br />
    He'd been a bit of a speed demon practicing the day before on cross country and<br />
    I told myself we'd only do what I
        felt comfortable doing.<br />
    With the laid back horse I had on Sunday, we just went out and had a good time.<br />
    In dressage he decided he didn't have a left lead, so we were low in the placings
    after that.<br />
    In stadium we had a blast and went clean and fast, which put us in first.<br />
    Cross country we kept a nice easy canter and only had a few spots where he showed
    he was getting tired.<br />
    But, the ditch that we'd fought over the week before slipped us up. When he refused
    the
    first time it went<br />
    through my head that we'd have to retire, but he accepted the smack of the whip
    without kicking out and<br />
    grudgingly went over it. With that we ended up in 4th place. Quite respectable for
    the stiff course at<br />
    an event I couldn't picture myself entering just a month earlier.<br />
  
            
