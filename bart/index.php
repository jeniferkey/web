<PRE>
    <B>The Author to the Reader</B>
<P>
    Reader, thou hast here an honest book; it doth at the outset forewarn
    thee that, in contriving the same, I have proposed to myself no other
    than a domestic and private end: I have had no consideration at all
    either to thy service or to my glory. My powers are not capable of
    any such design. I have dedicated it to the particular commodity of
    my kinsfolk and friends, so that, having lost me (which they must do
    shortly), they may therein recover some traits of my conditions and
    humours, and by that means preserve more whole, and more life-like,
    the knowledge they had of me. Had my intention been to seek the
    world's favour, I should surely have adorned myself with borrowed
    beauties: I desire therein to be viewed as I appear in mine own
    genuine, simple, and ordinary manner, without study and artifice: for
    it is myself I paint. My defects are therein to be read to the life,
    and my imperfections and my natural form, so far as public reverence
    hath permitted me. If I had lived among those nations, which (they
    say) yet dwell under the sweet liberty of nature's primitive laws, I
    assure thee I would most willingly have painted myself quite fully
    and quite naked. Thus, reader, myself am the matter of my book:
    there's no reason thou shouldst employ thy leisure about so frivolous
    and vain a subject. Therefore, farewell.

    <I>From </I>MONTAIGNE<I>, June 12, 1580</I>
</PRE>

